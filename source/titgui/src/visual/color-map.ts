export type ColorMapName = keyof typeof colorMaps;

// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

export function createColorMapTexture(
  colorMapName: ColorMapName,
  resolution = 256
): DataTexture {
  const points = colorMaps[colorMapName].points;

  const minVal = points[0][0];
  const maxVal = points[points.length - 1][0];
  const data = new Uint8Array(resolution * 4);
  for (let i = 0; i < resolution; ++i) {
    const value = minVal + (i * (maxVal - minVal)) / (resolution - 1);

    let lower = points[0];
    let upper = points[points.length - 1];
    for (let j = 0; j < points.length - 1; ++j) {
      if (points[j][0] <= value && value <= points[j + 1][0]) {
        lower = points[j];
        upper = points[j + 1];
        break;
      }
    }

    assert(upper[0] > lower[0]);
    const dt = (value - lower[0]) / (upper[0] - lower[0]);
    data[i * 4 + 0] = Math.round((lower[1] + dt * (upper[1] - lower[1])) * 255);
    data[i * 4 + 1] = Math.round((lower[2] + dt * (upper[2] - lower[2])) * 255);
    data[i * 4 + 2] = Math.round((lower[3] + dt * (upper[3] - lower[3])) * 255);
    data[i * 4 + 3] = 255;
  }

  const texture = new DataTexture(data, resolution, 1, RGBAFormat);
  texture.needsUpdate = true;
  texture.generateMipmaps = false;
  texture.minFilter = LinearFilter;
  texture.magFilter = LinearFilter;

  return texture;
}

